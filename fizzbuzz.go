package main

import "fmt"

/*
 * Write a short function that prints each number from 0 to 100 on a new line
 * For each multiple of 3 print the fizz
 * For each multiple of 5 print the buzz
 * For each multiple of 3 and 5 print the word fizzbuzz
 */

func main() {
	for i := 1; i < 100; i++ {
		if i%15 == 0 {
			fmt.Println("Fizz")
		} else if i%3 == 0 {
			fmt.Println("Buzz")
		} else if i%5 == 0 {
			fmt.Println("FizzBuzz")
		} else {
			fmt.Println(i)
		}
	}
}
